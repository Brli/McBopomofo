#!/bin/bash
perl -p -e 's/\r//g;s/∼//g' \
| sed -e 's/リーダーで見るために変換しています//
          s/まるごと//
          s/作者: .* 看板://
          s/^標題: //
          s/^時間: //
          s/※ 發信站: 批踢踢實業坊(ptt.cc)//' \
| gsed -e 's/[\!-ÿ]/ /g
           s/[	,　,è,ø,»,◆,‵,′,╧,￣,▂,ξ,──,□]/ /g
           s/[【,】,（,）,＜,＞,〔,〕,～,《,》,「,」,“,”,『,』]/ /g
           s/[〈,〉,┐,\—,┌,﹝,﹞,↓,↑,★,☆, ●,·,ˋ]/ /g
           s/[╰,┴,├,┤,╮,╭,│,┼,┬,╯,╞,═,╪,╡]/ /g
           s/[～,‧,。,，,！,、,：,；,＠,＃,＄,％,︿,＆,＊,’]/ /g
           s/[？,▼,▲,→,–,…,◎,※,＼,／,​,◄,►,．,←,↩,－,▽,⊙]/ /g
           s/[˙,︼,▃,︻,︶,▇,ψ,︱,﹀,︾,﹒,ˍ,﹀]/ /g
           s/[︷,〃,︸,〝,＿,▁]/ /g
           s/[╱,▉,█,▌,▋,▎,◤,▍,◢,▏,∕,▆,▄,▅,▊,◣,≒,◥]/ /g' \
|  sed -e 's/^ *//g;s/ *$//g;s/  */ /g' \
|  sed -e 's/麻痹/麻痺/g' \
|  sed -e 's/証/證/g' \
| grep -v -e '^$' -e '^ *$'
